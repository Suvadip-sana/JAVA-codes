package com.company;

public class j_08_03_increment_decrement_op {
    public static void main(String[] args) {
        int i = 56;
        System.out.println(i++);    //56
        System.out.println(i);      //57
        System.out.println(++i);    //58
        System.out.println(i);      //58

    }
}
